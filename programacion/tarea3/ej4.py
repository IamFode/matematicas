import turtle

# crear lienzo
v=turtle.Screen()

# Triangulo
t=turtle.Turtle()
t.pensize(7)
t.color("black")
t.up()
t.goto(-150,100)
t.down()
t.left(110)
t.fd(100)
t.left(140)
t.fd(100)
t.left(110)
t.fd(65)
t.hideturtle()

